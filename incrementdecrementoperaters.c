#include <stdio.h>
int main(void)
{
    // variable declarations
    int a = 5;
    int b = 10;
    // code
    printf("\n\n");
    printf("a = %d\n", a);
    printf("a = %d\n", a++);
    printf("a = %d\n ", a);
    printf("a = %d\n\n", ++a);
    printf("b = %d\n", b);
    printf("b = %d\n", b--);
    printf("b = %d\n", b);
    printf("b = %d\n\n", --b);
    return (0);
}